class SizeRange{
public static void main(String[] args){
	System.out.println("Data Type"+"\tSize"+"\tMin value"+"\t\tMax value");
	System.out.println("Byte"+"\t\t"+Byte.SIZE+"\t"+Byte.MIN_VALUE+"\t\t\t"+Byte.MAX_VALUE);
	System.out.println("Short"+"\t\t"+Short.SIZE+"\t"+Short.MIN_VALUE+"\t\t\t"+Short.MAX_VALUE);
	System.out.println("Integer"+"\t\t"+Integer.SIZE+"\t"+Integer.MIN_VALUE+"\t\t"+Integer.MAX_VALUE);
	System.out.println("Long"+"\t\t"+Long.SIZE+"\t"+Long.MIN_VALUE+"\t"+Long.MAX_VALUE);
	System.out.println("Float"+"\t\t"+Float.SIZE+"\t"+Float.MIN_VALUE+"\t\t\t"+Float.MAX_VALUE);
	System.out.println("Double"+"\t\t"+Double.SIZE+"\t"+Double.MIN_VALUE+"\t\t"+Double.MAX_VALUE);
	System.out.println("Character"+"\t"+Character.SIZE);
}
}